def print_feature_summary(tracks: pd.DataFrame):
    print("\n📐 Track Feature Summary Statistics:")
    numeric_cols = ["tempo_bpm", "valence", "energy", "acousticness",
                    "instrumentalness", "danceability", "speechiness", "loudness_db"]
    summary = tracks[numeric_cols].describe().round(3)
    print(summary.to_string())

    print("\n🔔 Notable distribution insights:")
    print(f"   Tracks with instrumentalness > 0.5: {(tracks['instrumentalness'] > 0.5).mean():.1%}")
    print(f"   Tracks with speechiness > 0.33:    {(tracks['speechiness'] > 0.33).mean():.1%}")
    print(f"   Tracks in major key (mode=1):       {(tracks['mode'] == 1).mean():.1%}")
    print(f"   Tracks in 4/4 time:                 {(tracks['time_signature'] == 4).mean():.1%}")


